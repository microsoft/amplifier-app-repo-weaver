"""Window → list of (filename, markdown_with_frontmatter) source documents.

Produces a small, high-signal set per window:

1. ONE ``<until>-changes.md`` — merged PRs + commit-volume digest.
   When ``repo_qualifier`` is set (multi-repo mode): ``<repo>-<until>-changes.md``.
2. UP TO ``max_modules`` ``module-<slug>-<until>.md`` files — snapshots of the
   top-level code directories most changed in the window.
   When ``repo_qualifier`` is set: ``module-<repo>-<slug>-<until>.md``.

**Single-repo** (``repo_qualifier=None``): filenames are unqualified — identical
to the historic behaviour, so existing ingested corpora are unaffected.

**Multi-repo** (``repo_qualifier`` provided): every filename and doc body carry
the repo identifier so wiki-weaver keeps pages from different repos distinct and
citations always point back to the right source.

Never fabricates provenance.  All data comes from git plumbing or the gh CLI.
"""

from __future__ import annotations

import re
import textwrap
from typing import Optional

from . import gitio

# Maximum number of files shown in a module inventory before truncating.
_MAX_INVENTORY_FILES = 60

# README filename candidates tried in order.
_README_CANDIDATES = [
    "README.md",
    "readme.md",
    "README.rst",
    "Readme.md",
    "docs/README.md",
]

# Maximum body chars shown per PR before truncating.
_PR_BODY_MAX = 600

# ---------------------------------------------------------------------------
# PR classification helpers  (ROUTINE vs SUBSTANTIVE)
# ---------------------------------------------------------------------------

# Any author login ending in [bot] is a bot (dependabot[bot], release-please[bot], …).
_ROUTINE_AUTHOR_RE = re.compile(r"\[bot\]$", re.IGNORECASE)

# Release/version-bump PRs authored by a human are still routine: they carry no design signal.
# Matches:
#   "chore: release v1.2.0"           — standard release-please output
#   "chore(main): release v1.2.0"     — release-please with branch scope
#   "chore(release): v0.7.0"          — manual release PR using scope=release
#   "chore: v0.7.0"                   — bare version bump commit
#
# NOTE: "chore(deps): upgrade frontend toolchain" is NOT routine when authored by a human —
# the scope is "deps" but the body is an intentional toolchain upgrade.  The distinction is
# enforced by the *bot-author* check above; this pattern only catches release/version titles.
_ROUTINE_HUMAN_TITLE_RE = re.compile(
    r"^chore(\([^)]*\))?\s*:\s*(release\b|v\d)", re.IGNORECASE
)

# Extracts the conventional-commit scope: "feat(frontend): …" → "frontend"
_CC_SCOPE_RE = re.compile(r"^[a-z]+\(([^)]+)\)", re.IGNORECASE)

# Extracts a short package name from "bump <pkg> from <ver> to <ver>" dependency-bump titles.
_DEP_BUMP_NAME_RE = re.compile(r"\bbump\s+([\w@./\-]+)\s+from\b", re.IGNORECASE)


def _is_routine_pr(pr: dict[str, object]) -> bool:
    """Return True if this PR carries little design signal (ROUTINE).

    ROUTINE:
    - Any bot-authored PR: author ``is_bot`` flag is True **or** login ends with ``[bot]``.
      (GitHub Apps like ``app/dependabot`` set ``is_bot=True``; classic ``dependabot[bot]``
      matches the login pattern — both are covered.)
    - Human-authored ``chore: release …`` PRs (auto-generated by release tooling).

    NOT routine (human-authored ``chore(deps)`` may be a deliberate toolchain upgrade):
    - PR titled ``chore(deps): upgrade frontend toolchain`` by a human → SUBSTANTIVE.
    """
    author_info = pr.get("author") or {}
    if isinstance(author_info, dict):
        # Prefer the explicit is_bot field present in gh CLI JSON responses.
        if author_info.get("is_bot") is True:
            return True
        login = str(author_info.get("login") or "")
    else:
        login = str(author_info)

    if _ROUTINE_AUTHOR_RE.search(login):
        return True

    title = str(pr.get("title") or "")
    return bool(_ROUTINE_HUMAN_TITLE_RE.match(title))


def _extract_cc_scope(title: str) -> str:
    """Return the conventional-commit scope from *title*, or ``''`` if absent.

    Examples::

        "feat(frontend): add dark mode"   → "frontend"
        "fix(auth): token expiry"         → "auth"
        "chore(deps): bump axios"         → "deps"
        "chore: release v1.0.0"           → ""
        "fix stale data in sidebar"       → ""
    """
    m = _CC_SCOPE_RE.match(title)
    return m.group(1).lower() if m else ""


def _extract_dep_name(title: str) -> str:
    """Extract a short package name from a dependency-bump PR title.

    ``"chore(deps): Bump axios from 0.21.1 to 0.21.4"``       → ``"axios"``
    ``"chore(deps-dev): Bump @types/react from 18.0 to 18.2"`` → ``"react"``

    Falls back to a trimmed fragment after the colon.
    """
    m = _DEP_BUMP_NAME_RE.search(title)
    if m:
        pkg = m.group(1)
        # "@scope/name" → "name" for brevity
        return pkg.split("/")[-1] if "/" in pkg else pkg
    if ":" in title:
        return title.split(":", 1)[-1].strip()[:30]
    return title[:30]


def _summarise_routine(routine_prs: list[dict[str, object]]) -> str:
    """Return a compact one-line aggregate description of routine (noise) PRs.

    Example output::

        "8 dependency bump(s) (form-data, dompurify, cryptography, ...); 2 release PR(s)"
    """
    dep_bumps: list[str] = []
    releases: int = 0

    for pr in routine_prs:
        title = str(pr.get("title") or "")
        if _ROUTINE_HUMAN_TITLE_RE.match(title):
            releases += 1
        else:
            dep_bumps.append(_extract_dep_name(title))

    fragments: list[str] = []
    if dep_bumps:
        # Show up to 6 unique package names; indicate truncation with "…"
        seen: list[str] = []
        for n in dep_bumps:
            if n and n not in seen:
                seen.append(n)
        shown = seen[:6]
        names_str = ", ".join(shown)
        if len(dep_bumps) > len(shown):
            names_str += ", ..."
        fragments.append(f"{len(dep_bumps)} dependency bump(s) ({names_str})")
    if releases:
        fragments.append(f"{releases} release PR(s)")
    if not fragments:
        fragments.append(f"{len(routine_prs)} routine PR(s)")

    return "; ".join(fragments)


def _append_pr_detail(parts: list[str], pr: dict[str, object]) -> None:
    """Append the full detail block for one substantive PR to *parts*."""
    n = pr.get("number", "?")
    title = pr.get("title") or "(no title)"
    raw_body = pr.get("body") or ""
    assert isinstance(raw_body, str)
    body_excerpt = (
        textwrap.shorten(raw_body.strip(), width=_PR_BODY_MAX, placeholder="\u2026")
        if raw_body.strip()
        else ""
    )
    author_info = pr.get("author") or {}
    if isinstance(author_info, dict):
        author_name = author_info.get("login") or "unknown"
    else:
        author_name = str(author_info)
    merged_at = str(pr.get("mergedAt") or "")[:10]
    files_list = pr.get("files") or []
    assert isinstance(files_list, list)
    file_count = len(files_list)
    top_paths = [str(f.get("path", "")) for f in files_list[:5] if isinstance(f, dict)]

    parts.append(f"### PR #{n}: {title}\n\n")
    parts.append(f"- **Author (PR opener):** {author_name}\n")
    parts.append(f"- **Merged:** {merged_at}\n")
    parts.append(f"- **Files changed:** {file_count}\n")
    if top_paths:
        parts.append(f"- **Key paths:** {', '.join(top_paths)}\n")
    if body_excerpt:
        parts.append(f"\n{body_excerpt}\n")
    parts.append("\n")


