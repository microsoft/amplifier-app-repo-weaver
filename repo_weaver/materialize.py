def materialize(
    repo: str,
    since: str,
    until: str,
    max_prs: int = 15,
    max_modules: int = 5,
    repo_qualifier: Optional[str] = None,
) -> list[tuple[str, str]]:
    """Materialize source documents for the window (since, until].

    Args:
        repo:           Absolute path to the local git repository.
        since:          Window start date YYYY-MM-DD (exclusive — commits *after*
                        this date are included).
        until:          Window end date YYYY-MM-DD (inclusive, up to 23:59:59).
        max_prs:        Maximum merged PRs to include in the change digest.
        max_modules:    Maximum module snapshot documents to emit.
        repo_qualifier: When non-None (multi-repo mode) this short label is
                        injected into every filename and document body so that
                        sources from different repos never collide and citations
                        always trace back to the correct repo.  Typically the
                        repository's directory base-name (e.g.
                        ``"amplifier-app-team-pulse"``).
                        When None (single-repo mode) filenames are unqualified
                        — identical to historic behaviour.

    Returns:
        List of ``(filename, content)`` pairs ready to write into ``_inbox/``.
        Filenames are date-prefixed slugs as recommended by wiki-weaver.
    """
    origin_url = gitio.get_origin_url(repo)
    owner_repo: Optional[tuple[str, str]] = None
    if origin_url:
        owner_repo = gitio.parse_owner_repo(origin_url)

    until_rev = gitio.get_window_rev(repo, until)
    commits = gitio.get_commits_name_only(repo, since, until)

    docs: list[tuple[str, str]] = []

    # 1. Change digest
    # Filename: "<repo>-<until>-changes.md" (multi) or "<until>-changes.md" (single)
    digest_content = _build_change_digest(
        repo, since, until, until_rev, commits, owner_repo, max_prs, repo_qualifier
    )
    digest_filename = (
        f"{repo_qualifier}-{until}-changes.md"
        if repo_qualifier
        else f"{until}-changes.md"
    )
    docs.append((digest_filename, digest_content))

    # 2. Module snapshots
    # Filenames: "module-<repo>-<slug>-<until>.md" (multi) or "module-<slug>-<until>.md" (single)
    module_docs = _build_module_snapshots(
        repo, since, until, until_rev, commits, owner_repo, max_modules, repo_qualifier
    )
    docs.extend(module_docs)

    return docs


# ---------------------------------------------------------------------------
# Frontmatter helpers
# ---------------------------------------------------------------------------


def _frontmatter(author: str, source: str, date_str: str) -> str:
    """Emit YAML frontmatter block with only the fields that have values."""
    lines = ["---"]
    if author:
        lines.append(f"author: {author}")
    if source:
        lines.append(f"source: {source}")
    if date_str:
        lines.append(f"date: {date_str}")
    lines.append("---")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Change digest
# ---------------------------------------------------------------------------


def _build_change_digest(
    repo: str,
    since: str,
    until: str,
    until_rev: Optional[str],
    commits: list[dict[str, object]],
    owner_repo: Optional[tuple[str, str]],
    max_prs: int,
    repo_qualifier: Optional[str] = None,
) -> str:
    """Build the change-digest source document.

    In single-repo mode (``repo_qualifier=None``) the document title is
    ``# Changes: <since> -> <until>``.  In multi-repo mode a
    ``**Repository:** <qualifier>`` line is added immediately after the
    title so the synthesizer can attribute facts to the right repo and keep
    pages for different repos distinct.

    Merged PRs are split into two tiers so routine noise never buries real work:

    - **Substantive**: human-authored, non-release PRs.  Every one gets full
      detail (number, title, author, date, files, body excerpt) and is grouped
      by conventional-commit scope so coordinated same-area change-sets read
      together.  All substantive PRs within the fetched pool are shown.
    - **Routine**: bot-authored dependency bumps and auto-generated release PRs.
      Collapsed into a single compact line (counts + package names).  They do
      **not** consume the ``max_prs`` budget.

    The ``max_prs`` parameter controls how many PRs are fetched from GitHub
    (used as a scaling hint).  All substantive PRs within that fetched pool are
    shown; only routine ones are collapsed.
    """
    parts: list[str] = []
    parts.append(f"# Changes: {since} \u2192 {until}\n\n")
    # In multi-repo mode, label the repo so the synthesizer keeps digests distinct.
    if repo_qualifier:
        parts.append(f"**Repository:** `{repo_qualifier}`\n\n")

    # Fetch generously so the window is fully covered even when many newer PRs
    # exist beyond it.  Routine PRs are free (collapsed), so we never need to
    # trade one class against the other.
    fetch_cap = min(max(max_prs * 8, 80), 200)
    prs: list[dict[str, object]] = []
    if owner_repo:
        owner, name = owner_repo
        prs = gitio.gh_merged_prs(f"{owner}/{name}", since, until, max_fetch=fetch_cap)

    if prs:
        substantive: list[dict[str, object]] = [p for p in prs if not _is_routine_pr(p)]
        routine: list[dict[str, object]] = [p for p in prs if _is_routine_pr(p)]

        # ---- Substantive Changes ----
        parts.append("## Substantive Changes\n\n")
        if substantive:
            parts.append(
                "_Grouped by conventional-commit scope so coordinated change-sets "
                "read together. Routine dependency bumps and release PRs are "
                "collapsed in the section below — they do not consume this budget._\n\n"
            )
            # Group by CC scope; preserve the API's merged-at order within each group.
            scoped: dict[str, list[dict[str, object]]] = {}
            for pr in substantive:
                scope = _extract_cc_scope(str(pr.get("title") or ""))
                bucket = scope or "(general)"
                if bucket not in scoped:
                    scoped[bucket] = []
                scoped[bucket].append(pr)

            for scope_key, scope_prs in scoped.items():
                parts.append(f"### Area: `{scope_key}`\n\n")
                for pr in scope_prs:
                    _append_pr_detail(parts, pr)
        else:
            parts.append("_(No substantive PRs found in this window.)_\n\n")

        # ---- Routine Maintenance ----
        parts.append("## Routine Maintenance\n\n")
        if routine:
            summary = _summarise_routine(routine)
            parts.append(f"Routine maintenance this window: {summary}.\n\n")
        else:
            parts.append("_(None in this window.)_\n\n")

    else:
        parts.append(
            "## Merged Pull Requests\n\n"
            "_(None found in this window, or gh CLI unavailable.)_\n\n"
        )

    # ---- Commit-volume summary ----
    dir_counts: dict[str, int] = {}
    for commit in commits:
        paths_obj = commit.get("paths", [])
        assert isinstance(paths_obj, list)
        seen_dirs: set[str] = set()
        for p in paths_obj:
            assert isinstance(p, str)
            parts_path = p.split("/")
            top_dir = parts_path[0] if len(parts_path) > 1 else "(root)"
            if top_dir not in seen_dirs:
                dir_counts[top_dir] = dir_counts.get(top_dir, 0) + 1
                seen_dirs.add(top_dir)

    total_commits = len(commits)
    parts.append(
        f"## Commit Volume Summary ({since} \u2192 {until})\n\n"
        f"Total commits in window: {total_commits}\n\n"
    )
    if dir_counts:
        parts.append("Commits by top-level directory:\n\n")
        for d, count in sorted(dir_counts.items(), key=lambda x: -x[1]):
            parts.append(f"- `{d}`: {count} commit(s)\n")
        parts.append("\n")

    # ---- Window contributors (provenance, NOT per-PR authorship) ----
    # Appended to the body BEFORE the frontmatter is composed so the section
    # is clearly visible in the document — and explicitly NOT the PR-opener field.
    authors = gitio.get_shortlog_authors(repo, since, until, top_n=3)
    if authors:
        parts.append("## Window Contributors\n\n")
        parts.append(
            "_Git-log contributors in this window (window-level provenance — "
            "NOT the per-PR opener; see each PR's `Author (PR opener)` field "
            "for authoritative per-PR attribution.)_\n\n"
        )
        parts.append(", ".join(authors) + "\n\n")

    # ---- Frontmatter ----
    # Use only the single top contributor so the `author:` metadata field cannot
    # be mistaken for a list of PR authors by downstream synthesizers.
    fm_author = authors[0] if authors else ""
    source_url = ""
    if owner_repo:
        owner, name = owner_repo
        source_url = f"https://github.com/{owner}/{name}/pulls?q=is:pr+is:merged"

    fm = _frontmatter(fm_author, source_url, until)
    return fm + "\n\n" + "".join(parts)


